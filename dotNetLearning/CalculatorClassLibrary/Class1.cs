using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CalculatorClassLibrary
{
    public class Calculator
    {
        public int Sum(int fno, int sno)
        {
            return fno + sno;
        }

        public int Subtract(int fno, int sno)
        {
            return fno - sno;
        }

    }
}
